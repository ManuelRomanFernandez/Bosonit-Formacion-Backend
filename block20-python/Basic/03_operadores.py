### Operadores comparativos ###
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print("--------")

# Ordena alfabeticamente por ASCII
print("Holaaaaa" > "Python")
print("Holaaaaa" < "Python")
print("Holaaaaa" >= "Python")
print("Holaaaaa" <= "Python")

print("---------")

# Cuenta caracteres
print(len("Holaaaaa") > len("Python"))
print(len("Holaaaaa") < len("Python"))
print(len("Holaaaaa") >= len("Python"))
print(len("Holaaaaa") <= len("Python"))

print("----------")

### Operadores lógicos ###

print(3 > 4 and len("Holaaaaa") > len("Python"))
print(3 < 4 and len("Holaaaaa") > len("Python"))
print(3 > 4 and len("Holaaaaa") < len("Python"))
print(3 < 4 and len("Holaaaaa") < len("Python"))
print(3 > 4 or len("Holaaaaa") > len("Python"))
print(3 < 4 or len("Holaaaaa") > len("Python"))
print(3 > 4 or len("Holaaaaa") < len("Python"))
print(3 < 4 or len("Holaaaaa") < len("Python"))

